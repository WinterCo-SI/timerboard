type SampleTimerFixture = [
  offsetMinutes: number,
  region: string,
  system: string,
  name: string,
  structure: string,
  owner: string,
  state: string,
  tag: string,
];

function pad2(value: number): string {
  return String(value).padStart(2, '0');
}

function formatUtcDateTime(date: Date): { date: string; time: string } {
  return {
    date: date.toISOString().slice(0, 10),
    time: `${pad2(date.getUTCHours())}:${pad2(date.getUTCMinutes())}`,
  };
}

function fixtureCountdown(date: Date, generatedAtMs: number): string {
  const ms = date.getTime() - generatedAtMs;
  if (ms <= 0) return 'elapsed';

  const totalMinutes = Math.max(1, Math.round(ms / 60000));
  const days = Math.floor(totalMinutes / 1440);
  const hours = Math.floor((totalMinutes % 1440) / 60);
  const minutes = totalMinutes % 60;

  if (days) return `${days}d ${hours}h`;
  if (hours) return `${hours}h ${minutes}m`;
  return `${minutes}m`;
}

function makeFixtureLine(
  generatedAtMs: number,
  [
    offsetMinutes,
    region,
    system,
    name,
    structure,
    owner,
    state,
    tag,
  ]: SampleTimerFixture,
): string {
  const date = new Date(generatedAtMs + offsetMinutes * 60000);
  const parts = formatUtcDateTime(date);

  return `${parts.date} ${parts.time} [${region}] ${system} - ${name} [${structure}][${owner}][${state}] (${fixtureCountdown(date, generatedAtMs)}) [#${tag}]`;
}

